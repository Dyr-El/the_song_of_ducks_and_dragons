def check_child(child, parent1, parent2):
    return all(c in (p1, p2) for c, p1, p2 in zip(child, parent1, parent2))


def child_similarity(child, parent):
    matches = sum(c == p for c, p in zip(child, parent))
    return matches


def find_parents(dna_sequences):
    d = {}
    for c in range(len(dna_sequences)):
        for p1 in range(len(dna_sequences)):
            if c == p1:
                continue
            for p2 in range(p1 + 1, len(dna_sequences)):
                if c == p2:
                    continue
                if check_child(dna_sequences[c], dna_sequences[p1], dna_sequences[p2]):
                    d[c] = (p1, p2)
    return d


def parse_input(inp):
    lines = inp.strip().split('\n')
    sequences = [line.split(':')[1] for line in lines]
    return sequences


def total_similarity(inp):
    sequences = parse_input(inp)
    parents = find_parents(sequences)
    total_sim = 0
    for child in parents:
        total_sim += (child_similarity(sequences[child], sequences[parents[child][0]]) *
                      child_similarity(sequences[child], sequences[parents[child][1]]))
    return total_sim


