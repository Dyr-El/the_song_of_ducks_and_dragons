def map_volcato(inp):
    volcano = None
    grid = dict()
    for yidx, line in enumerate(inp.splitlines()):
        for xidx, ch in enumerate(line):
            if ch == '@':
                volcano = (xidx, yidx)
            elif ch.isdigit():
                grid[(xidx, yidx)] = int(ch)
    return volcano, grid
    

def sum_of_cells(inp, radius):
    volcano, grid = map_volcato(inp)
    return sum(value for (x, y), value in grid.items()
               if abs(x - volcano[0])**2 + abs(y - volcano[1])**2 <= radius**2)

